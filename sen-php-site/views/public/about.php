<!-- Histoire et mission -->
<section class="py-5">
    <div class="container">
        <div class="row">
            <div class="col-lg-6 mb-4 mb-lg-0">
                <h2 class="section-title border-bottom pb-2 mb-4">Notre histoire</h2>
                <p>Fondée en 1957, l'Université Nationale Cheikh Hamidou Kane (UNCHK) est l'une des plus anciennes et prestigieuses institutions d'enseignement supérieur d'Afrique de l'Ouest. Nommée en l'honneur de l'écrivain et philosophe sénégalais Cheikh Hamidou Kane, notre université a formé des générations de leaders, chercheurs et professionnels qui ont contribué au développement du pays et de la région.</p>
                <p>Au fil des décennies, l'UNCHK s'est développée pour devenir un centre d'excellence académique reconnu internationalement, tout en restant fidèle à sa mission d'éducation accessible et de qualité.</p>
            </div>
            <div class="col-lg-6">
                <h2 class="section-title border-bottom pb-2 mb-4">Notre mission</h2>
                <p>L'UNCHK a pour mission de former des citoyens responsables et des professionnels compétents, capables de contribuer au développement durable de leur communauté et de leur pays.</p>
                <p>Nous nous engageons à :</p>
                <ul>
                    <li>Offrir une éducation de qualité, accessible et inclusive</li>
                    <li>Promouvoir la recherche scientifique et l'innovation</li>
                    <li>Favoriser l'engagement communautaire et le service à la société</li>
                    <li>Cultiver un environnement d'apprentissage multiculturel et respectueux</li>
                </ul>
            </div>
        </div>
    </div>
